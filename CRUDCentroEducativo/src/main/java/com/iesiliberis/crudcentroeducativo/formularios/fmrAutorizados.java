     private void configTabla() {

        String col[] = {"ID", "DNI", "NOMBRE", "APELLIDO1", "APELLIDO2", "PARENTESCO"};
        DefaultTableModel modelo = new DefaultTableModel(col, 0);
        jtAutorizados.setModel(modelo);
        jtAutorizados.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }
     
     private void cargaTabla() {
        DefaultTableModel modelo = (DefaultTableModel) jtAutorizados.getModel();

        AutorizadosDaoImp alumControler = AutorizadosDaoImp.getInstance();
        String[] fila = new String[6];

        modelo.setNumRows(0);
        try {
            List<Autorizados> lst = alumControler.getAll();

            for (Autorizados alum : lst) {
                fila[0] = "" + alum.getId();
                fila[1] = "" + alum.getDni();
                fila[2] = "" + alum.getNombre();
                fila[3] = "" + alum.getApellido1();
                fila[4] = "" + alum.getApellido2();
                fila[5] = "" + alum.getParentesco();
                
                modelo.addRow(fila);
            }
            //selecciono la primera fila
            if (modelo.getRowCount() > 0) {
                jtAutorizados.setRowSelectionInterval(0, 0);
                setCampos();
            }

        } catch (Exception e) {
            System.out.println("Error:" + e.getMessage());
        }
    }
     
     
      private void setCampos() {

        txtId.setText(jtAutorizados.getValueAt(jtAutorizados.getSelectedRow(), 0).toString());
        txtDni.setText(jtAutorizados.getValueAt(jtAutorizados.getSelectedRow(), 1).toString());
        txtNombre.setText(jtAutorizados.getValueAt(jtAutorizados.getSelectedRow(), 2).toString());
        txtApellido1.setText(jtAutorizados.getValueAt(jtAutorizados.getSelectedRow(), 3).toString());
        txtApellido2.setText(jtAutorizados.getValueAt(jtAutorizados.getSelectedRow(), 4).toString());
        txtParentesco.setText(jtAutorizados.getValueAt(jtAutorizados.getSelectedRow(), 5).toString());
        

    }
      
     private Autorizados getCampos() {
        Autorizados a = new Autorizados();

        a.setId(Integer.parseInt(txtId.getText()));
        a.setDni(txtDni.getText());
        a.setNombre(txtNombre.getText());
        a.setApellido1(txtApellido1.getText());
        a.setApellido2(txtApellido2.getText());
        a.setParentesco(txtParentesco.getText());
       
        return a;
    }
